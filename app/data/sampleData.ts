export function getSessionEvents(sessionId: string): Event[] {
  return sampleEvents.filter(e => e.sessionId === sessionId);
}

export function getSessionMetrics(sessionId: string): Metrics | undefined {
  return sampleMetrics.find(m => m.sessionId === sessionId);
}

export function getSessionReport(sessionId: string): FeedbackReport | undefined {
  return sampleReports.find(r => r.sessionId === sessionId);
}

export function getStudentSessions(participantId: string): Session[] {
  return sampleSessions.filter(s => s.participantId === participantId);
}